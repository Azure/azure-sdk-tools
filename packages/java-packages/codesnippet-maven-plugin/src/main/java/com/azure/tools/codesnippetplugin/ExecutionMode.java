/**
 * Execution modes that the codesnippet plugin supports.
 */
public enum ExecutionMode {
    /**
     * Updates the injected codesnippets.
     */
    UPDATE,

    /**
     * Verifies the injected codesnippets.
     */
    VERIFY
}
